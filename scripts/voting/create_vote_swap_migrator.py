# --- RPC + keystore helpers --------------------------------------------------

def _rpc(method, params):
    req = urllib.request.Request(
        NETWORK,
        data=json.dumps(
            {"jsonrpc": "2.0", "id": 1, "method": method, "params": params}
        ).encode(),
        headers={"Content-Type": "application/json"},
    )
    r = json.loads(urllib.request.urlopen(req, timeout=120).read())
    if r.get("error"):
        raise RuntimeError(f"RPC {method}: {r['error']}")
    return r["result"]


def _keystore_path(name: str) -> str:
    return os.path.expanduser(
        os.path.join("~", ".brownie", "accounts", name + ".json"))


def keystore_address(name: str) -> str:
    with open(_keystore_path(name)) as f:
        return to_checksum_address("0x" + json.load(f)["address"])


def account_load(name: str):
    with open(_keystore_path(name)) as f:
        pkey = account.decode_keyfile_json(json.load(f), getpass())
    return account.Account.from_key(pkey)


# --- contract handles --------------------------------------------------------

def load_contracts():
    factory = boa.load_partial("contracts/Factory.vy").at(YB_FACTORY)
    factory_owner = boa.load_partial("contracts/HybridFactoryOwner.vy").at(
        factory.admin())
    voting = boa.load_abi(
        os.path.join(ABI_DIR, "TokenVoting.abi.json"), name="AragonVoting"
    ).at(VOTING_PLUGIN)
    ve = boa.load_partial("contracts/dao/VotingEscrow.vy").at(
        voting.getVotingToken())
    return factory, factory_owner, voting, ve


# --- forked-test: passing + executing pending votes --------------------------

def _scan_deploy_votes(latest_block: int) -> list[int]:
    """deploy_yb_pools_v3.py proposal ids in creation order, filtered by
    creator so unrelated pending proposals don't sneak in."""
    creators = {keystore_address(n).lower() for n in DEPLOY_PROPOSERS}
    seen = {}
    hi = latest_block
    while hi > 0 and len(seen) < len(DEPLOY_PROPOSERS):
        lo = max(hi - LOG_CHUNK + 1, 0)
        for log in _rpc("eth_getLogs", [{
            "address": VOTING_PLUGIN, "topics": [PROPOSAL_CREATED_TOPIC0],
            "fromBlock": hex(lo), "toBlock": hex(hi),
        }]):
            creator = "0x" + log["topics"][2][26:]
            if creator.lower() not in creators:
                continue
            seen[int(log["topics"][1], 16)] = int(log["blockNumber"], 16)
        hi = lo - 1
    return sorted(seen, key=lambda p: seen[p])


def pass_vote(voting, pid: int):
    cast = 0
    for voter in TEST_VOTERS:
        if voting.isMinParticipationReached(pid):
            break
        if not voting.canVote(pid, voter, VOTE_YES):
            continue
        with boa.env.prank(voter):
            voting.vote(pid, VOTE_YES, False)
        cast += 1
    if not voting.isMinParticipationReached(pid):
        raise RuntimeError(f"Proposal {pid} did not reach quorum "
                           f"({cast} whales voted)")
    print(f"    {cast} whale(s) voted YES; quorum reached")


def execute_pending(voting, proposal_ids: list[int]):
    pending = [pid for pid in proposal_ids if not voting.getProposal(pid)[1]]
    if not pending:
        return
    print(f"  voting on {len(pending)} pending proposal(s)...")
    for pid in pending:
        pass_vote(voting, pid)
    boa.env.time_travel(seconds=voting.minDuration() + 3600)
    for pid in pending:
        if voting.getProposal(pid)[1]:
            continue
        assert voting.canExecute(pid), f"proposal {pid} cannot execute"
        voting.execute(pid)


# --- the swap vote -----------------------------------------------------------

def build_vote_actions(factory_owner, new_migrator: str) -> list[Action]:
    return [
        Action(to=factory_owner.address, value=0,
               data=factory_owner.set_limit_setter.prepare_calldata(
                   new_migrator, True)),
        Action(to=factory_owner.address, value=0,
               data=factory_owner.set_limit_setter.prepare_calldata(
                   PREVIOUS_LT_MIGRATOR, False)),
    ]


def vote_metadata(new_migrator: str) -> dict:
    return {
        "title": "YB v3: swap LTMigrator (limit=1 fallback fix)",
        "summary": (
            f"Register the new LTMigrator {new_migrator} as a limit setter "
            "on HybridFactoryOwner and disable the previously-registered "
            f"LTMigrator {PREVIOUS_LT_MIGRATOR} (Vote installed via "
            "https://yieldbasis.com/proposal/37). The previous build's "
            "lt_allocate_stablecoins(lt_from, 0) deallocation reverts "
            "('Not disabled') after a HybridVault withdrawal flips "
            "disabled_lts back to False on a deprecated LT, trapping "
            "users mid-migration. The new build branches on disabled_lts "
            "and falls back to limit=1 in that case, leaving lt_from at "
            "the sentinel allocation=1 so migrate-back is refused."
        ),
        "resources": [],
    }


# --- IPFS / Etherscan --------------------------------------------------------

def pin_to_ipfs(content: dict) -> str:
    response = requests.post(
        "https://api.pinata.cloud/pinning/pinJSONToIPFS",
        json={"pinataContent": content,
              "pinataMetadata": {"name": "pinnie.json"},
              "pinataOptions": {"cidVersion": 1}},
        headers={"Authorization": f"Bearer {PINATA_TOKEN}",
                 "Content-Type": "application/json"},
        timeout=30,
    )
    assert 200 <= response.status_code < 400, response.text
    return "ipfs://" + response.json()["IpfsHash"]


def verify_on_etherscan(contract, etherscan):
    while True:
        try:
            sleep(VERIFY_RETRY_SECONDS)
            boa_verify(contract, etherscan, wait=True)
            return
        except ValueError as e:
            print(e)
            if "Already Verified" in str(e):
                return


