def run_test():
    boa.fork(NETWORK, block_identifier="latest")
    latest_block = boa.env.evm.patch.block_number
    factory, factory_owner, voting, ve = load_contracts()

    print("=== Executing pending deploy_yb_pools_v3 votes on the fork ===")
    deploy_pids = _scan_deploy_votes(latest_block)
    if len(deploy_pids) != len(DEPLOY_PROPOSERS):
        raise RuntimeError(
            f"Expected {len(DEPLOY_PROPOSERS)} deploy proposals, "
            f"found {len(deploy_pids)}")
    execute_pending(voting, deploy_pids)
    assert factory_owner.limit_setters(PREVIOUS_LT_MIGRATOR), (
        f"{PREVIOUS_LT_MIGRATOR} is not a limit_setter after Vote 1 — "
        "hardcoded address is wrong?")
    print(f"  previous (buggy) migrator: {PREVIOUS_LT_MIGRATOR}")

    print("\n=== Deploying new LTMigrator ===")
    new_migrator = boa.load(
        "contracts/LTMigrator.vy", factory.STABLECOIN(),
        factory_owner.address)
    print(f"  new migrator: {new_migrator.address}")

    print("\n=== Checkpointing VotingEscrow ===")
    ve.checkpoint()

    proposer = keystore_address(PROPOSER_KEYSTORE)
    print(f"\n=== Creating the swap vote from {PROPOSER_KEYSTORE} "
          f"({proposer}) ===")
    actions = build_vote_actions(factory_owner, new_migrator.address)
    with boa.env.prank(proposer):
        pid = voting.createProposal(*Proposal(
            metadata=b"", actions=actions, allowFailureMap=0,
            startDate=0, endDate=0, voteOption=0, tryEarlyExecution=True))
    print(f"  created proposal {pid}")

    print("\n=== Passing and executing the swap vote ===")
    pass_vote(voting, pid)
    boa.env.time_travel(seconds=voting.minDuration() + 3600)
    assert voting.canExecute(pid), "swap vote cannot execute"
    voting.execute(pid)

    print("\n=== Post-execution state ===")
    new_is = factory_owner.limit_setters(new_migrator.address)
    prev_is = factory_owner.limit_setters(PREVIOUS_LT_MIGRATOR)
    print(f"  limit_setters[new  {new_migrator.address}] = {new_is}")
    print(f"  limit_setters[prev {PREVIOUS_LT_MIGRATOR}] = {prev_is}")
    assert new_is and not prev_is, "swap state mismatch"
    print("\nMigrator swap works end-to-end.")


def run_prod():
    boa.set_network_env(NETWORK)
    boa.env.add_account(account_load(PROPOSER_KEYSTORE), force_eoa=True)
    factory, factory_owner, voting, ve = load_contracts()

    etherscan = Etherscan(api_key=ETHERSCAN_API_KEY)
    print("=== Deploying new LTMigrator ===")
    new_migrator = boa.load(
        "contracts/LTMigrator.vy", factory.STABLECOIN(),
        factory_owner.address)
    print(f"  new migrator: {new_migrator.address}")

    print(f"\n=== Verifying {new_migrator.address} on Etherscan ===")
    verify_on_etherscan(new_migrator, etherscan)
    print("  verified")

    print("\n=== Checkpointing VotingEscrow ===")
    ve.checkpoint()

    print("=== Creating the swap vote ===")
    metadata = pin_to_ipfs(vote_metadata(new_migrator.address)).encode()
    pid = voting.createProposal(*Proposal(
        metadata=metadata,
        actions=build_vote_actions(factory_owner, new_migrator.address),
        allowFailureMap=0,
        startDate=0, endDate=0, voteOption=0, tryEarlyExecution=True))
    print(f"Created proposal: {pid}")


if __name__ == "__main__":
    if "--test" in sys.argv[1:]:
        run_test()
    else:
        run_prod()
