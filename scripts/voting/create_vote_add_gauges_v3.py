# --- voting-plugin helpers (forked test) -------------------------------------

def _rpc(method, params):
    req = urllib.request.Request(
        NETWORK,
        data=json.dumps(
            {"jsonrpc": "2.0", "id": 1, "method": method, "params": params}
        ).encode(),
        headers={"Content-Type": "application/json"},
    )
    r = json.loads(urllib.request.urlopen(req, timeout=120).read())
    if r.get("error"):
        raise RuntimeError(f"RPC {method}: {r['error']}")
    return r["result"]


def discover_pending_votes(latest_block: int) -> list[int]:
    """The 6 most-recently-created proposal ids, in creation order. TokenVoting
    proposal ids are hashes, so they are read from ProposalCreated logs;
    scanned backwards from the head, stopping once 6 are in hand."""
    seen = {}
    hi = latest_block
    while hi > 0 and len(seen) < 6:
        lo = max(hi - LOG_CHUNK + 1, 0)
        for log in _rpc("eth_getLogs", [{
            "address": VOTING_PLUGIN, "topics": [PROPOSAL_CREATED_TOPIC0],
            "fromBlock": hex(lo), "toBlock": hex(hi),
        }]):
            seen[int(log["topics"][1], 16)] = int(log["blockNumber"], 16)
        hi = lo - 1
    if len(seen) < 6:
        raise RuntimeError(f"Found only {len(seen)} ProposalCreated logs")
    return sorted(seen, key=lambda p: seen[p])[-6:]


def pass_vote(voting, pid: int):
    """Cast YES with the test whales until the proposal reaches minimum
    participation (support is 100% since every vote is YES)."""
    cast = 0
    for voter in TEST_VOTERS:
        if voting.isMinParticipationReached(pid):
            break
        if not voting.canVote(pid, voter, VOTE_YES):
            continue
        with boa.env.prank(voter):
            voting.vote(pid, VOTE_YES, False)
        cast += 1
    if not voting.isMinParticipationReached(pid):
        raise RuntimeError(f"Proposal {pid} did not reach quorum "
                           f"({cast} whales voted)")
    print(f"    {cast} whale(s) voted YES; quorum reached")


# --- IPFS / accounts (production) --------------------------------------------

def pin_to_ipfs(content: dict) -> str:
    response = requests.post(
        "https://api.pinata.cloud/pinning/pinJSONToIPFS",
        json={"pinataContent": content,
              "pinataMetadata": {"name": "pinnie.json"},
              "pinataOptions": {"cidVersion": 1}},
        headers={"Authorization": f"Bearer {PINATA_TOKEN}",
                 "Content-Type": "application/json"},
        timeout=30,
    )
    assert 200 <= response.status_code < 400, response.text
    return "ipfs://" + response.json()["IpfsHash"]


def account_load(name: str):
    path = os.path.expanduser(
        os.path.join("~", ".brownie", "accounts", name + ".json"))
    with open(path) as f:
        pkey = account.decode_keyfile_json(json.load(f), getpass())
    return account.Account.from_key(pkey)


