def run_test():
    boa.fork(NETWORK)
    latest_block = boa.env.evm.patch.block_number
    factory, comparator, gc, voting, ve = load_contracts()

    # The guard must revert while the gauges still have no code.
    print("=== Negative test: comparator guard before gauges exist ===")
    g0 = GAUGES[0]
    try:
        comparator.check_equal(g0["gauge"], LP_TOKEN_SELECTOR,
                               int(g0["lt"], 16))
        raise AssertionError("Guard passed though the gauge does not exist!")
    except Exception as e:
        if "Guard passed" in str(e):
            raise
        print(f"  Correctly reverted: {e}")

    # Execute the 6 pending votes through the plugin.
    print("\n=== Executing the 6 pending votes via the plugin ===")
    pending = discover_pending_votes(latest_block)
    print(f"  pending proposal ids: {pending}")
    for i, pid in enumerate(pending, start=1):
        if voting.getProposal(pid)[1]:
            print(f"  Vote {i} already executed - skipping")
            continue
        print(f"  Vote {i} ({pid}): voting…")
        pass_vote(voting, pid)
    boa.env.time_travel(seconds=voting.minDuration() + 3600)
    for i, pid in enumerate(pending, start=1):
        if voting.getProposal(pid)[1]:
            continue
        assert voting.canExecute(pid), f"Vote {i} cannot execute"
        voting.execute(pid)
        print(f"  Vote {i} executed.")

    # Predicted gauges must now exist on-chain.
    print("\n=== Verifying gauges against the created markets ===")
    for g in GAUGES:
        on_chain = factory.markets(g["market"]).staker
        assert on_chain.lower() == g["gauge"].lower(), (
            f"{g['label']}: hard-coded {g['gauge']} != on-chain {on_chain}")
        print(f"  {g['label']:6s} gauge {g['gauge']} OK")

    print("\n=== Checkpointing VotingEscrow ===")
    ve.checkpoint()

    print(f"\n=== Creating the gauge vote from {TEST_PROPOSER} ===")
    actions = build_vote_actions(comparator, gc)
    with boa.env.prank(TEST_PROPOSER):
        pid7 = voting.createProposal(*Proposal(
            metadata=b"", actions=actions, allowFailureMap=0,
            startDate=0, endDate=0, voteOption=0, tryEarlyExecution=True))
    print(f"  created proposal {pid7}")

    print("\n=== Passing and executing the gauge vote ===")
    pass_vote(voting, pid7)
    boa.env.time_travel(seconds=voting.minDuration() + 3600)
    assert voting.canExecute(pid7), "gauge vote cannot execute"
    voting.execute(pid7)

    print("\n=== Result: GaugeController state ===")
    ok = True
    for g in GAUGES:
        added = gc.time_weight(g["gauge"]) != 0
        ok &= added
        print(f"  {g['label']:6s} {g['gauge']}  "
              f"{'REGISTERED' if added else 'MISSING'}")
    print("\n" + ("ALL 4 GAUGES REGISTERED - vote works end-to-end."
                  if ok else "FAILED - some gauges not registered."))


def run_prod():
    if not PROD_PROPOSER_KEYSTORE:
        sys.exit("Set PROD_PROPOSER_KEYSTORE to the hot-wallet keystore name.")
    boa.set_network_env(NETWORK)
    boa.env.add_account(account_load(PROD_PROPOSER_KEYSTORE), force_eoa=True)
    factory, comparator, gc, voting, ve = load_contracts()

    print("=== Checkpointing VotingEscrow ===")
    ve.checkpoint()

    print("=== Creating the gauge vote ===")
    pid = voting.createProposal(*Proposal(
        metadata=pin_to_ipfs(vote_metadata()).encode(),
        actions=build_vote_actions(comparator, gc), allowFailureMap=0,
        startDate=0, endDate=0, voteOption=0, tryEarlyExecution=True))
    print(f"Created proposal: {pid}")


if __name__ == "__main__":
    if "--test" in sys.argv[1:]:
        run_test()
    else:
        run_prod()
