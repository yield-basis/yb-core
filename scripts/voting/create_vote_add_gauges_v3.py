# --- contract handles --------------------------------------------------------

def load_contracts():
    factory = boa.load_partial("contracts/Factory.vy").at(YB_FACTORY)
    comparator = boa.load_partial("contracts/dao/CallComparator.vy").at(
        CALL_COMPARATOR)
    gc = boa.load_partial("contracts/dao/GaugeController.vy").at(
        factory.gauge_controller())
    voting = boa.load_abi(
        os.path.join(ABI_DIR, "TokenVoting.abi.json"), name="AragonVoting"
    ).at(VOTING_PLUGIN)
    ve = boa.load_partial("contracts/dao/VotingEscrow.vy").at(
        voting.getVotingToken())
    assert gc.owner() == YB_DAO, f"GaugeController owner is {gc.owner()}"
    return factory, comparator, gc, voting, ve


# --- the vote ----------------------------------------------------------------

def build_vote_actions(comparator, gc) -> list[Action]:
    """4 CallComparator existence guards followed by 4 GaugeController.add_gauge
    calls. Each guard reverts unless the gauge exists and its LP_TOKEN() equals
    the expected LT, so the whole vote reverts atomically if any gauge is
    missing or sits at an unexpected address at execution time."""
    actions = [
        Action(to=comparator.address, value=0,
               data=comparator.check_equal.prepare_calldata(
                   g["gauge"], LP_TOKEN_SELECTOR, int(g["lt"], 16)))
        for g in GAUGES
    ]
    actions += [
        Action(to=gc.address, value=0,
               data=gc.add_gauge.prepare_calldata(g["gauge"]))
        for g in GAUGES
    ]
    return actions


