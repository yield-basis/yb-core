# --- keystore helpers --------------------------------------------------------

def _keystore_path(name: str) -> str:
    return os.path.expanduser(
        os.path.join("~", ".brownie", "accounts", name + ".json"))


def keystore_address(name: str) -> str:
    with open(_keystore_path(name)) as f:
        return to_checksum_address("0x" + json.load(f)["address"])


def account_load(name: str):
    with open(_keystore_path(name)) as f:
        pkey = account.decode_keyfile_json(json.load(f), getpass())
    return account.Account.from_key(pkey)


# --- contract handles --------------------------------------------------------

def load_contracts():
    factory = boa.load_partial("contracts/Factory.vy").at(YB_FACTORY)
    # The migrator MUST be wired to the live Factory admin, whatever it is now.
    factory_owner = boa.load_partial("contracts/HybridFactoryOwner.vy").at(
        factory.admin())
    voting = boa.load_abi(
        os.path.join(ABI_DIR, "TokenVoting.abi.json"), name="AragonVoting"
    ).at(VOTING_PLUGIN)
    ve = boa.load_partial("contracts/dao/VotingEscrow.vy").at(
        voting.getVotingToken())
    return factory, factory_owner, voting, ve


# --- forked-test: passing + executing a vote ---------------------------------

def pass_vote(voting, pid: int):
    cast = 0
    for voter in TEST_VOTERS:
        if voting.isMinParticipationReached(pid):
            break
        if not voting.canVote(pid, voter, VOTE_YES):
            continue
        with boa.env.prank(voter):
            voting.vote(pid, VOTE_YES, False)
        cast += 1
    if not voting.isMinParticipationReached(pid):
        raise RuntimeError(f"Proposal {pid} did not reach quorum "
                           f"({cast} whales voted)")
    print(f"    {cast} whale(s) voted YES; quorum reached")


# --- the swap vote -----------------------------------------------------------

def build_vote_actions(factory_owner, new_migrator: str) -> list:
    return [
        Action(to=factory_owner.address, value=0,
               data=factory_owner.set_limit_setter.prepare_calldata(
                   new_migrator, True)),
        Action(to=factory_owner.address, value=0,
               data=factory_owner.set_limit_setter.prepare_calldata(
                   BROKEN_LT_MIGRATOR, False)),
    ]


def vote_metadata(new_migrator: str, factory_owner: str) -> dict:
    return {
        "title": "YB v3: redeploy LTMigrator against the current Factory owner",
        "summary": (
            f"Register the new LTMigrator {new_migrator} (wired to the current "
            f"Factory owner {factory_owner}) as a limit setter on that owner, "
            f"and disable the stale migrator {BROKEN_LT_MIGRATOR}. The stale "
            "migrator's immutable FACTORY_OWNER points at the previous owner "
            "(0x0f4e...), which is no longer the Factory admin, so every "
            "migration through it reverts 'Access' when the LT's _check_admin "
            "rejects the stale owner. FACTORY_OWNER is immutable, so the only "
            "fix is to redeploy against the current owner and re-point the "
            "limit-setter registration."
        ),
        "resources": [],
    }


# --- IPFS / Etherscan --------------------------------------------------------

def pin_to_ipfs(content: dict) -> str:
    response = requests.post(
        "https://api.pinata.cloud/pinning/pinJSONToIPFS",
        json={"pinataContent": content,
              "pinataMetadata": {"name": "pinnie.json"},
              "pinataOptions": {"cidVersion": 1}},
        headers={"Authorization": f"Bearer {PINATA_TOKEN}",
                 "Content-Type": "application/json"},
        timeout=30,
    )
    assert 200 <= response.status_code < 400, response.text
    return "ipfs://" + response.json()["IpfsHash"]


def verify_on_etherscan(contract, etherscan):
    while True:
        try:
            sleep(VERIFY_RETRY_SECONDS)
            boa_verify(contract, etherscan, wait=True)
            return
        except ValueError as e:
            print(e)
            if "Already Verified" in str(e):
                return


