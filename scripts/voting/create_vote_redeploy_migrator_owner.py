#!/usr/bin/env python3
"""
Deploy a fresh LTMigrator wired to the CURRENT Factory owner and create a YB
DAO vote that registers it as a limit setter on that owner while disabling the
stale-owner migrator BROKEN_LT_MIGRATOR.

Why this is needed
------------------
The currently-registered migrator BROKEN_LT_MIGRATOR (0xE707...) was deployed
with its immutable FACTORY_OWNER pointing at a now-stale HybridFactoryOwner
(0x0f4e...). The Factory admin has since moved to the new/fixed owner
(0xb8BA...). When the migrator routes lt_allocate_stablecoins through the stale
owner, the LT's _check_admin only accepts the *current* Factory admin, so every
migration reverts "Access" (reproduced in tests_forked/test_migrator_access.py).

FACTORY_OWNER is immutable, so the migrator cannot be repaired in place -- it
must be redeployed against the current owner. Registering the redeploy as a
limit setter is ADMIN-only on the owner, and that ADMIN is the DAO Ownership
agent, so this step REQUIRES A DAO VOTE. Both the enable (new) and disable
(broken) happen inside one proposal so the swap is atomic.

Usage:
  python scripts/voting/create_vote_redeploy_migrator_owner.py          # prod
  python scripts/voting/create_vote_redeploy_migrator_owner.py --test   # fork

--test forks mainnet at head, deploys the new migrator, creates this swap
proposal, passes + executes it via the test whales, asserts the limit_setters
mapping flipped, and then runs the actual reported user migration
(0xD24C... : LT 0xfBF3... -> 0x651D...) end-to-end to prove it now succeeds.
"""
